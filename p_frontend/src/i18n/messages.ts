function replaceParams(template: string, params?: MessageParams): string {
  if (!params) {
    return template
  }
  return Object.entries(params).reduce(
    (text, [key, value]) => text.replaceAll(`{${key}}`, String(value)),
    template,
  )
}

export function translateText(text: string, locale: SupportedLocale, params?: MessageParams): string {
  if (locale === 'zh-CN') {
    return replaceParams(text, params)
  }
  return replaceParams(enUsMessages[text] ?? text, params)
}

export function useI18n() {
  const locale = useI18nStore((state) => state.locale)
  const t = useCallback(
    (text: string, params?: MessageParams) => translateText(text, locale, params),
    [locale],
  )

  return {
    locale,
    t,
  }
}
